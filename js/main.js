function setProjectDetail(name)
{
    language = getSessionLanguage()

    doc = getProjectDetailJSON(name)
    
    setProjectDetailSub(doc.intro, "intro", false)
    setProjectDetailSub(doc.context, "context", false)
    setProjectDetailSub(doc.mainTask, "mainTask", true)
}

function setProjectDetailSub(doc, tag, multipleDesc)
{
    let title = document.getElementById(tag+"_title")
    title.innerHTML = doc.title
    
    let sub = document.getElementById(tag+"_sub")
    sub.innerHTML = doc.sub

    img = document.getElementById(tag+"_image")
    img.src = doc.image.path
    img.parentElement.className = doc.image.class 

    let itemsInList = Object.keys(doc.list).length
    let subList = Object.values(doc.list)
    
    for(i = 0; i < itemsInList; ++i)
        document.getElementById(tag+"_list"+i).innerHTML = subList[i]
    
    
    if(!multipleDesc)
    {
        let desc = document.getElementById(tag+"_desc")
        desc.innerHTML = doc.desc
    }
    else
    {
        let subCount = Object.keys(doc.desc.descSubTitles).length
        let values = Object.values(doc.desc.descSubDescriptions)
        setProjectDetailDesc(tag, doc.desc, subCount)
    }
}

function setProjectDetailDesc(tag, doc, count)
{
    let titles = Object.values(doc.descSubTitles)
    let descs = Object.values(doc.descSubDescriptions)

    document.getElementById(tag+"_pre").innerHTML = doc.pre

    for(i = 0; i < count; ++i)
    {
        document.getElementById(tag+"_title"+i).innerHTML = titles[i]
        document.getElementById(tag+"_desc"+i).innerHTML = descs[i]
    }
}

