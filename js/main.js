function setSessionLanguage(language, page, projectDetailName)
{
    sessionStorage.setItem("language", language)

    switch(page) {
        case "Index":
        setLanguageIndex();
        break;

        case "Projects":
        setLanguageProjects();
        break;

        case "ProjectDetail":
        setLanguageProjectDetail(projectDetailName);
        break;

        case "Contact":
        setLanguageContact();
        break;

        case "Legal":
        setLanguageLegal();
        break;
    }
}

function getSessionLanguage()
{
    if(sessionStorage.getItem("language") != undefined)
        return sessionStorage.getItem("language")

    if(navigator.language === "fr-FR")
        return 'fr'
    
    return 'en'

}

